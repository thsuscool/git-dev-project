public class KosisDetailOption extends AbsQuery<Map> {
	private static final Log logger = LogFactory.getLog(KosisDetailOption.class);
	@Override
	public String getApiId() {
		return "6004";
	}

	@Override
	public HttpMethod getHttpMethod() {
		return HttpMethod.POST;
	}

	@Override
	public Class getMustParameter() throws AbsException {
		return MustParam.class;
	}

	@Override
	public Class getOptionParameter() throws AbsException {
		return OptionParam.class;
	}

	@Override
	protected String getQueryStr() {
		return null;
	}
	
	enum MustParam
	{
		list_id,
		org_id
	}
	
	enum OptionParam
	{
		title,
		gis_se
	}
	
	@Override
	public Map executeAPI(HttpServletRequest req, HttpServletResponse res,
			String trId) throws AbsException {
		// TODO Auto-generated method stub
		httpSession = req.getSession();

		Map resultData = new HashMap();
		
		try {
			logger.info("START Query - ApiID[" + this.getApiId() + "] ");

			Map mapParameter = getParameterMap(req);

			_checkNullParameterValue(mapParameter);
			
			List kosisMenu = session.selectList("kosis_menu.getKosisdetailOption", mapParameter);
			
			if( kosisMenu == null || kosisMenu.size() <= 0 ){
				String seq = session.selectOne("kosis_menu.selectError", mapParameter);
				
				if( seq == null ){
					session.insert("kosis_menu.insertError", mapParameter);
				} else {
					mapParameter.put("seq", seq);
					session.update("kosis_menu.updateError", mapParameter);
				}
			} 
			
			resultData.put("kosis_detail_option", kosisMenu);
		
			logger.info("END Query - TXID[" + getApiId() + "] ");
		} catch (AbsAPIException e) {
			logger.error(e);
			throw e;
		} catch (IllegalArgumentException e) {
			logger.error(e);
			throw new ApiException("입력값을 체크 해 주세요");
		} catch (Exception e) {
			logger.error(e);
			throw new ApiException(StringUtil.getErrMsg());
		}
		return resultData;
	}
}